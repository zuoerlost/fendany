public class RuntimeCmdBean {

    private String sout;

    private String eout;

    private boolean isSuccess;

    public String getSout() {
        return sout;
    }

    public void setSout(String sout) {
        this.sout = sout;
    }

    public String getEout() {
        return eout;
    }

    public void setEout(String eout) {
        this.eout = eout;
    }

    public boolean isSuccess() {
        return isSuccess;
    }

    public void setSuccess(boolean success) {
        isSuccess = success;
    }
}
